si = lambda: input().strip()
ii = lambda: int(si())
lsi = lambda: list(input().strip().split())
lii = lambda: list(map(int,input().strip().split()))

class Solution:
    def run(self):
        n = ii()
        arr = lii()

        ans = 0
        for i in range(1,n):
            if(arr[i]>=arr[i-1]):continue
            t = arr[i]^arr[i-1]
            ans = max(ans,1<<(t.bit_length()-1))
            arr[i]|=1<<(t.bit_length()-1)
        return ans

