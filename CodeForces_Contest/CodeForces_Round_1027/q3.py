si = lambda: input().strip()
ii = lambda: int(si())
lsi = lambda: list(input().strip().split())
lii = lambda: list(map(int,input().strip().split()))

class Solution:
    def run(self):
        n = ii()
        arr = lii()

        cnt = 1
        ans = 0
        for i in range(1,n):
            if(arr[i]==arr[i-1]):continue
            elif(arr[i]==arr[i-1]+1):cnt+=1
            else:
                ans+=(cnt+1)//2
                cnt = 1
        ans+=(cnt+1)//2
        return ans

